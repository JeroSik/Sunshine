function updateIcon(icon) {
  if (icon == "clear-day") {
    return "images/icons/icon-2.svg";
  } else if (icon == "clear-night") {
    return "images/icons/icon-1.svg";
  } else if (icon == "rain") {
    return "images/icons/icon-9.svg";
  } else if (icon == "snow") {
    return "images/icons/icon-13.svg";
  } else if (icon == "sleet") {
    return "images/icons/icon-13.svg";
  } else if (icon == "wind") {
    return "images/icons/icon-7.svg";
  } else if (icon == "fog") {
    return "images/icons/icon-7.svg";
  } else if (icon == "cloudy") {
    return "images/icons/icon-5.svg";
  } else if (icon == "partly-cloudy-day") {
    return "images/icons/icon-5.svg";
  } else if (icon == "partly-cloudy-night") {
    return "images/icons/icon-5.svg";
  } else if (icon == "hail") {
    return "images/icons/icon-14.svg";
  } else if (icon == "thunderstorm") {
    return "images/icons/icon-12.svg";
  } else if (icon == "tornado") {
    return "images/icons/icon-8.svg";
  }
}
